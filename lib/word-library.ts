/**
 * Get all categories (metadata only, without words).
 */
export function getCategories(): Omit<WordCategory, 'words'>[] {
  return wordLibrary.map(({ words, ...rest }) => ({
    ...rest,
    wordCount: words.length,
  } as any))
}

/**
 * Get a single category by ID with all its words.
 */
export function getCategoryById(id: string): WordCategory | undefined {
  return wordLibrary.find((c) => c.id === id)
}

/**
 * Get all words across all categories.
 */
export function getAllWords(): (WordEntry & { categoryId: string })[] {
  return wordLibrary.flatMap((c) =>
    c.words.map((w) => ({ ...w, categoryId: c.id }))
  )
}

/**
 * Search words by query string across all categories.
 */
export function searchWords(query: string): (WordEntry & { categoryId: string; categoryName: string })[] {
  const lower = query.toLowerCase().trim()
  if (!lower) return []
  return wordLibrary.flatMap((c) =>
    c.words
      .filter((w) => w.word.toLowerCase().includes(lower) || w.definition.toLowerCase().includes(lower))
      .map((w) => ({ ...w, categoryId: c.id, categoryName: c.name }))
  )
}

/**
 * Get words filtered by difficulty level.
 */
export function getWordsByDifficulty(difficulty: WordEntry['difficulty']): (WordEntry & { categoryId: string })[] {
  return wordLibrary.flatMap((c) =>
    c.words
      .filter((w) => w.difficulty === difficulty)
      .map((w) => ({ ...w, categoryId: c.id }))
  )
}
